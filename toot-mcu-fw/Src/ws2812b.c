/* Initializes the pixel data array */
void initWS2812B(ws2812b_t* string, SPI_HandleTypeDef* spiHandle, pindef_t spiCS, uint16_t numPixels)
{
	string->spiHandle = spiHandle;
	string->spiCS = spiCS;
	string->numPixels = numPixels;
	// 9 bytes per pixel + 2 empty bytes on beginning and end
	string->numBytes = (numPixels<<3) + numPixels + 4;
	string->pixelDataArray = (uint8_t *)malloc(string->numBytes);
	string->pixelDataArray[0] = 0; //Clear the preamble bytes
	string->pixelDataArray[1] = 0; //Clear the preamble bytes
	string->pixelDataArray[string->numBytes - 1] = 0; //Clear the post send cleardown bytes.
	string->pixelDataArray[string->numBytes - 2] = 0; //Clear the post send cleardown bytes.
}

/* Sets a specific pixel to a specific r,g,b color */
void setPixelColor(ws2812b_t* string, uint16_t n, uint8_t r, uint8_t g, uint8_t b)
{
   uint8_t *bptr = string->pixelDataArray + (n<<3) + n + 2;
   uint8_t *tPtr = (uint8_t *)encoderLookup + r*2 + r;

   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;

   tPtr = (uint8_t *)encoderLookup + g*2 + g;
   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;

   tPtr = (uint8_t *)encoderLookup + b*2 + b;
   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;
   *bptr++ = *tPtr++;
}

/* Sets a specific pixel to a specific hex RGB color */
void setPixelColorHex(ws2812b_t* string, uint16_t n, uint32_t c)
{
	uint8_t r,g,b;

	r = (uint8_t)(c >> 16);
	g = (uint8_t)(c >>  8);
	b = (uint8_t)c;

	uint8_t *bptr = string->pixelDataArray + (n<<3) + n + 2;
	uint8_t *tPtr = (uint8_t *)encoderLookup + g*2 + g;

	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;

	tPtr = (uint8_t *)encoderLookup + r*2 + r;
	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;

	tPtr = (uint8_t *)encoderLookup + b*2 + b;
	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;
	*bptr++ = *tPtr++;
}

/* Clear pixels */
void clearPixels(ws2812b_t* string)
{
	// Note the first byte in the buffer is a preamble
	uint8_t *bptr = string->pixelDataArray + 2;
	uint8_t *tPtr;

	for(int i=0;i<(string->numPixels*3);i++)
	{
	   tPtr = (uint8_t *)encoderLookup;
   	   *bptr++ = *tPtr++;
	   *bptr++ = *tPtr++;
	   *bptr++ = *tPtr++;
	}
}

/* Update pixels */
void showPixels(ws2812b_t* string)
{
	// This function will block if another SPI operation is in progress
	while(HAL_SPI_GetState(string->spiHandle)!=HAL_SPI_STATE_READY);

	HAL_GPIO_WritePin(string->spiCS.port, string->spiCS.pin, 0);
	if (HAL_SPI_Transmit_DMA(string->spiHandle, string->pixelDataArray, string->numBytes)!= HAL_OK)
	{
		/* Transfer error in transmission process */
	}
	// Write 0 to CS pin done in DMA callback
}
